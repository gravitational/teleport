"""Tiny mock OIDC provider for regression tests.

Each test user has their own issuer path (e.g. http://mock-oidc:8080/alice).
The mock auto-issues an authorization code on /authorize (no UI), and the
token endpoint returns an ID token whose claims are derived from the issuer
path + user-roles configured via environment.

Per-user role config is read from /etc/mock-oidc/users.json, which is
generated by `cluster/setup-user.sh` when registering users. Format:

  {"alice": {"groups": ["requester"]}, "bob": {"groups": ["reviewer"]}}

The file is reread on every /token request so adding users at runtime works
without a restart.
"""
import base64
import json
import os
import secrets
import time
import urllib.parse
from http.server import BaseHTTPRequestHandler, HTTPServer
from socketserver import ThreadingMixIn

from cryptography.hazmat.primitives import serialization
from cryptography.hazmat.primitives.asymmetric import rsa
import jwt

BIND_HOST = os.environ.get("BIND_HOST", "0.0.0.0")
BIND_PORT = int(os.environ.get("BIND_PORT", "8080"))
EXTERNAL_HOST = os.environ.get("EXTERNAL_HOST", "mock-oidc:8080")
USERS_FILE = "/etc/mock-oidc/users.json"

PRIVATE_KEY = rsa.generate_private_key(public_exponent=65537, key_size=2048)
PRIVATE_PEM = PRIVATE_KEY.private_bytes(
    encoding=serialization.Encoding.PEM,
    format=serialization.PrivateFormat.PKCS8,
    encryption_algorithm=serialization.NoEncryption(),
)
PUBLIC_NUMBERS = PRIVATE_KEY.public_key().public_numbers()
KID = "regression-test-key"

CODES = {}  # code -> (user, nonce, client_id)


def b64uint(n: int) -> str:
    blen = (n.bit_length() + 7) // 8
    return base64.urlsafe_b64encode(n.to_bytes(blen, "big")).rstrip(b"=").decode()


def jwks() -> dict:
    return {"keys": [{
        "kty": "RSA",
        "use": "sig",
        "alg": "RS256",
        "kid": KID,
        "n": b64uint(PUBLIC_NUMBERS.n),
        "e": b64uint(PUBLIC_NUMBERS.e),
    }]}


def issuer_url(user: str) -> str:
    return f"http://{EXTERNAL_HOST}/{user}"


def discovery(user: str) -> dict:
    iss = issuer_url(user)
    return {
        "issuer": iss,
        "authorization_endpoint": f"{iss}/authorize",
        "token_endpoint": f"{iss}/token",
        "jwks_uri": f"{iss}/jwks",
        "response_types_supported": ["code"],
        "subject_types_supported": ["public"],
        "id_token_signing_alg_values_supported": ["RS256"],
        "scopes_supported": ["openid", "email", "profile", "groups"],
    }


def load_users() -> dict:
    try:
        with open(USERS_FILE) as f:
            return json.load(f)
    except FileNotFoundError:
        return {}


class Handler(BaseHTTPRequestHandler):
    def log_message(self, fmt, *args):
        # Quieter than the default which logs every request to stderr.
        print(f"{self.address_string()} {fmt % args}", flush=True)

    def respond(self, body, status=200, ctype="application/json"):
        if isinstance(body, dict):
            body = json.dumps(body).encode()
        elif isinstance(body, str):
            body = body.encode()
        self.send_response(status)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def redirect(self, url, status=302):
        self.send_response(status)
        self.send_header("Location", url)
        self.send_header("Content-Length", "0")
        self.end_headers()

    def parse_path(self):
        path, _, query = self.path.partition("?")
        return path, dict(urllib.parse.parse_qsl(query))

    def do_GET(self):
        path, params = self.parse_path()
        parts = [p for p in path.split("/") if p]
        if len(parts) >= 2 and parts[0] == ".well-known" and parts[1] == "openid-configuration":
            # /.well-known/openid-configuration without a user prefix — error
            return self.respond({"error": "user prefix required"}, 404)

        if len(parts) >= 1:
            user = parts[0]
            rest = "/" + "/".join(parts[1:])
            if rest == "/.well-known/openid-configuration":
                return self.respond(discovery(user))
            if rest == "/jwks":
                return self.respond(jwks())
            if rest == "/authorize":
                redirect_uri = params.get("redirect_uri")
                state = params.get("state", "")
                nonce = params.get("nonce", "")
                client_id = params.get("client_id", "")
                if not redirect_uri:
                    return self.respond({"error": "missing redirect_uri"}, 400)
                code = secrets.token_urlsafe(16)
                CODES[code] = (user, nonce, client_id)
                sep = "&" if "?" in redirect_uri else "?"
                return self.redirect(f"{redirect_uri}{sep}code={code}&state={urllib.parse.quote(state)}")

        self.respond({"error": "not found", "path": path}, 404)

    def do_POST(self):
        path, _ = self.parse_path()
        parts = [p for p in path.split("/") if p]
        length = int(self.headers.get("Content-Length", 0))
        raw = self.rfile.read(length).decode() if length else ""
        form = dict(urllib.parse.parse_qsl(raw))

        if len(parts) >= 2 and parts[1] == "token":
            user = parts[0]
            code = form.get("code")
            stored = CODES.pop(code, None)
            if not stored:
                return self.respond({"error": "invalid_grant", "error_description": "unknown code"}, 400)
            stored_user, nonce, client_id = stored
            if stored_user != user:
                return self.respond({"error": "invalid_grant", "error_description": "user mismatch"}, 400)
            users = load_users()
            extra = users.get(user, {})
            now = int(time.time())
            claims = {
                "iss": issuer_url(user),
                "aud": form.get("client_id") or client_id or "teleport",
                "iat": now,
                "exp": now + 3600,
                "sub": user,
                "email": f"{user}@regression.local",
            }
            if nonce:
                claims["nonce"] = nonce
            claims.update(extra)
            id_token = jwt.encode(claims, PRIVATE_PEM, algorithm="RS256", headers={"kid": KID})
            return self.respond({
                "access_token": secrets.token_urlsafe(16),
                "token_type": "Bearer",
                "id_token": id_token,
                "expires_in": 3600,
            })

        self.respond({"error": "not found", "path": path}, 404)


class ThreadingHTTPServer(ThreadingMixIn, HTTPServer):
    daemon_threads = True


if __name__ == "__main__":
    print(f"mock-oidc listening on {BIND_HOST}:{BIND_PORT}; external={EXTERNAL_HOST}", flush=True)
    ThreadingHTTPServer((BIND_HOST, BIND_PORT), Handler).serve_forever()
