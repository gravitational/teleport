package main

import (
	"os"
	"text/template"

	"github.com/coreos/go-semver/semver"
)

var outTemplate = template.Must(template.New("apiversion").Parse(
	`// Code generated by "make version". DO NOT EDIT.

package api

const Version = "{{.}}"

const VersionMajor = {{.Major}}
const VersionMinor = {{.Minor}}
const VersionPatch = {{.Patch}}
const VersionPreRelease = "{{.PreRelease}}"
const VersionMetadata = "{{.Metadata}}"
`))

func main() {
	v := semver.New(os.Args[1])
	if os.Args[1] != v.String() {
		panic("impossible semver parse")
	}

	outTemplate.Execute(os.Stdout, v)
}
