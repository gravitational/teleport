/*
 * This file implements Plan Modification for SAML IdP Service Provider.
 * It's not in saml_idp_service_provider.go because that's a generated file.
 */

package provider

import (
	"context"
	"encoding/xml"

	"github.com/beevik/etree"
	"github.com/hashicorp/terraform-plugin-framework/path"
	"github.com/hashicorp/terraform-plugin-framework/tfsdk"
	"github.com/hashicorp/terraform-plugin-framework/types"
)

// samlIdPEntityDescriptorXML parses the SAML entity descriptor XML.
// We don't generate an entity_descriptor, but we need to extract entity_id to
// match fields set by the API.
// See https://docs.oasis-open.org/security/saml/v2.0/saml-schema-metadata-2.0.xsd
// for official format
type samlIdPEntityDescriptorXML struct {
	EntityID         string                      `xml:"entityID,attr"`
	SPSSODescriptors []samlIdPSPSSODescriptorXML `xml:"SPSSODescriptor"`
}

type samlIdPSPSSODescriptorXML struct {
	AssertionConsumerServices  []samlIdPAssertionConsumerServiceXML `xml:"AssertionConsumerService"`
	AttributeConsumingServices []struct{}                           `xml:"AttributeConsumingService"`
}

type samlIdPAssertionConsumerServiceXML struct {
	Location string `xml:"Location,attr"`
}

func parseSamlIdPEntityDescriptor(descriptor string) (*samlIdPEntityDescriptorXML, error) {
	var ed samlIdPEntityDescriptorXML
	if err := xml.Unmarshal([]byte(descriptor), &ed); err != nil {
		return nil, err
	}
	return &ed, nil
}

func rewriteSamlIdPDescriptorElements(descriptor string, rewrite func(*etree.Element) bool) (string, error) {
	doc := etree.NewDocument()
	if err := doc.ReadFromString(descriptor); err != nil {
		return descriptor, err
	}
	// Matches what the API does. If we're replacing values, we'll generally
	// be operating on XML previously generated by the API
	doc.WriteSettings.CanonicalEndTags = true
	doc.WriteSettings.AttrSingleQuote = false

	var visit func(*etree.Element) bool
	visit = func(element *etree.Element) bool {
		if rewrite(element) {
			return true
		}
		for _, child := range element.ChildElements() {
			if visit(child) {
				return true
			}
		}
		return false
	}

	root := doc.Root()
	if root == nil {
		return descriptor, nil
	}
	visit(root)

	output, err := doc.WriteToString()
	if err != nil {
		return descriptor, err
	}

	return output, nil
}

func replaceEntityIDInSamlIdPDescriptor(descriptor, newEntityID string) (string, error) {
	return rewriteSamlIdPDescriptorElements(descriptor, func(element *etree.Element) bool {
		if element.Tag != "EntityDescriptor" {
			return false
		}
		if attr := element.SelectAttr("entityID"); attr != nil {
			attr.Value = newEntityID
			return true
		}
		return false
	})
}

func replaceACSURLInSamlIdPDescriptor(descriptor, newACSURL string) (string, error) {
	return rewriteSamlIdPDescriptorElements(descriptor, func(element *etree.Element) bool {
		if element.Tag != "AssertionConsumerService" {
			return false
		}
		if attr := element.SelectAttr("Location"); attr != nil && attr.Value != "" {
			attr.Value = newACSURL
			return true
		}
		return false
	})
}

// ModifyPlan synchronizes computed fields to match what the API will set.
// The SAML IdP API requires both entity_descriptor and entity_id for updates,
// and the entity_id value must match. If the terraform config provides only one
// of these, the update API call from terraform will still submit the generated
// value for both from the state. But it needs to ensure the entity_id of the
// previously computed value from the state is updated to match whichever value
// the user provided in the config.
//
// Note also: Behaviors here may need modifying (possibly simplifying) when
// https://github.com/gravitational/teleport/issues/64651 is fixed. As-is,
// the provider's codegen fails to preserve API updates to fields, but only on
// update calls.
func (r resourceTeleportSAMLIdPServiceProvider) ModifyPlan(ctx context.Context, req tfsdk.ModifyResourcePlanRequest, resp *tfsdk.ModifyResourcePlanResponse) {
	// Destroy: no op
	if req.Plan.Raw.IsNull() {
		return
	}
	// Create: The API generates entity_descriptor if the user didn't provide it
	// so they'll match -- no need to modify the plan
	if req.State.Raw.IsNull() {
		return
	}

	// Entity Descriptor values
	var specEntityDescriptorPath = path.Root("spec").AtName("entity_descriptor")
	var configEntityDescriptor types.String
	req.Config.GetAttribute(ctx, specEntityDescriptorPath, &configEntityDescriptor)
	var stateEntityDescriptor types.String
	req.State.GetAttribute(ctx, specEntityDescriptorPath, &stateEntityDescriptor)

	// Entity ID Values
	var specEntityIDPath = path.Root("spec").AtName("entity_id")
	var configEntityID types.String
	req.Config.GetAttribute(ctx, specEntityIDPath, &configEntityID)

	// ACS URL Values
	var specACSURLPath = path.Root("spec").AtName("acs_url")
	var configACSURL types.String
	req.Config.GetAttribute(ctx, specACSURLPath, &configACSURL)

	// If only one of entity_id and entity_descriptor is provided, set the other
	// one to match. This is required because the API rejects requests if the
	// two copies of the Entity ID don't match, combined with updates requiring
	// `entity_descriptor`, which forces the provider to pull it from the state
	// if the user didn't provide it.
	//
	// TODO(https://github.com/gravitational/teleport/issues/64498):
	// Consider setting resp.RequiresReplace = true if any of the involved
	// fields change once the provider correctly deals with the Teleport auth
	// cache. This would pushes all modifications of the resource to the API.
	if !configEntityDescriptor.IsNull() {
		// If we have an entity_descriptor, make sure entity_id matches, to
		// prevent the API from rejecting the call
		if configEntityID.IsNull() {
			if configEntityDescriptor.IsUnknown() {
				resp.Diagnostics.AddWarning("Unknown Entity Descriptor",
					"The entity_descriptor is unknown at plan time, so the provider cannot synchronize "+
						"the generated entity_id value with it. Changes to the entity_descriptor "+
						"after initial creation will fail due to this mismatch. To resolve this, "+
						"set the entity_id from your entity_descriptor explicitly in your terraform code.")
			} else {
				ed, err := parseSamlIdPEntityDescriptor(configEntityDescriptor.Value)
				if err != nil {
					resp.Diagnostics.AddError("Invalid entity_descriptor", err.Error())
					return
				}
				resp.Plan.SetAttribute(ctx, specEntityIDPath, types.String{Value: ed.EntityID})
			}
		}

		// `acs_url` need not be updated from the entity_descriptor because the
		// API neither does that nor validates that they match. The API does set
		// it to "" rather than null, but this does not cause Terraform  to
		// taint the resource not reapply on the next run.

	} else { // configEntityDescriptor.IsNull()
		// For updates, the API requires a valid `entity_descriptor`, but here the user hasn't
		// provided one in Terraform. We therefore start with the state and add in the entity_id and acs_url
		// provided in Terraform. For entity_id, this is required to ensure updates aren't rejected because
		// the values don't match. The API performs no such checks for `acs_url`, but replacing it here
		// works around API behavior that allows creating a Teleport resource with non-matching acs_url
		// values (https://github.com/gravitational/teleport/issues/64643).
		entityDescriptorXml := stateEntityDescriptor.Value
		var err error
		if configEntityID.IsUnknown() {
			resp.Diagnostics.AddWarning("Unknown Entity ID",
				"The entity_id is unknown at plan time, so the provider cannot synchronize "+
					"the generated entity_descriptor value with it. Changes to the entity_id "+
					"after initial creation will fail due to this mismatch. To resolve this, "+
					"copy the entity_descriptor using `terraform state show` and set it "+
					"explicitly in your terraform code, ensuring its Entity ID matches "+
					"the terraform entity_id field.")
		} else if !configEntityID.IsNull() {
			entityDescriptorXml, err = replaceEntityIDInSamlIdPDescriptor(entityDescriptorXml, configEntityID.Value)
			if err != nil {
				resp.Diagnostics.AddWarning("Failed to parse entity_descriptor as XML",
					"Could not set entity_id in entity_descriptor from state")
			}
		}
		if configACSURL.IsUnknown() {
			resp.Diagnostics.AddWarning("Unknown ACS URL",
				"The ACS URL is unknown at plan time, so the provider cannot synchronize "+
					"the generated entity_descriptor value with it. Changes to the acs_url "+
					"after initial creation will fail due to this mismatch. To resolve this, "+
					"copy the entity_descriptor using `terraform state show` and set it "+
					"explicitly in your terraform code, ensuring its ACS URL matches "+
					"the terraform acs_url field.")
		} else if !configACSURL.IsNull() {
			entityDescriptorXml, err = replaceACSURLInSamlIdPDescriptor(entityDescriptorXml, configACSURL.Value)
			if err != nil {
				resp.Diagnostics.AddWarning("Failed to parse entity_descriptor as XML",
					"Could not set acs_url in entity_descriptor from state")
			}
		}
		// Note: if neither entity_id nor acs_url were null, this will be a no-op
		resp.Plan.SetAttribute(ctx, specEntityDescriptorPath, types.String{Value: entityDescriptorXml})
	}
}
