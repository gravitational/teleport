#!/usr/bin/env bash
# Translates the Rust access list verification code to Lean4 using Aeneas.
#
# Prerequisites:
#   - charon (from https://github.com/AeneasVerif/charon)
#   - aeneas (from https://github.com/AeneasVerif/aeneas)
#
# See the README.md for installation instructions.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
POC_DIR="$(dirname "$SCRIPT_DIR")"
RUST_DIR="$POC_DIR/rust"
LEAN_DIR="$POC_DIR/lean4/FormalVerification"
LLBC_FILE="$RUST_DIR/accesslist_verify.llbc"

# Check prerequisites
for cmd in charon aeneas; do
    if ! command -v "$cmd" &>/dev/null; then
        echo "Error: $cmd not found. See README.md for installation instructions."
        exit 1
    fi
done

echo "==> Step 1: Extracting LLBC from Rust crate with charon..."
cd "$RUST_DIR"
# Use --no-default-features to exclude the FFI/serde dependencies
# which are not needed for verification and may confuse Aeneas.
# Use --dest-file to output the .llbc in the crate dir (default is workspace root).
charon cargo --preset=aeneas --dest-file "$LLBC_FILE" -- --no-default-features

if [ ! -f "$LLBC_FILE" ]; then
    echo "Error: No .llbc file generated by charon at $LLBC_FILE"
    exit 1
fi

echo "==> Step 2: Translating LLBC to Lean4 with aeneas..."
aeneas -backend lean "$LLBC_FILE" -dest "$LEAN_DIR"

echo "==> Translation complete!"
echo "Generated Lean4 files in: $LEAN_DIR/"
ls -la "$LEAN_DIR/"*.lean 2>/dev/null || echo "(no .lean files found - check for errors above)"
