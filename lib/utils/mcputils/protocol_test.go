func TestIDString(t *testing.T) {
	assert.Equal(t, "", IDString(jsonrpc.ID{}))
	assert.Equal(t, "12345", IDString(mustMakeIntID(t, 12345)))
	assert.Equal(t, "string-id", IDString(mustMakeID(t, "string-id")))
}
