package main

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"

	"github.com/gravitational/teleport/e2e/runner/fixtures"
)

func main() {
	all := fixtures.All()
	if len(all) == 0 {
		fmt.Fprintln(os.Stderr, "no fixtures registered")
		os.Exit(1)
	}

	var buf strings.Builder
	buf.WriteString(`/**
 * Teleport
 * Copyright (C) 2026  Gravitational, Inc.
 *
 * This program is free software: you can redistribute it and/or modify
 * it under the terms of the GNU Affero General Public License as published by
 * the Free Software Foundation, either version 3 of the License, or
 * (at your option) any later version.
 *
 * This program is distributed in the hope that it will be useful,
 * but WITHOUT ANY WARRANTY; without even the implied warranty of
 * MERCHANTABILITY or FITNESS FOR A PARTICULAR PURPOSE.  See the
 * GNU Affero General Public License for more details.
 *
 * You should have received a copy of the GNU Affero General Public License
 * along with this program.  If not, see <http://www.gnu.org/licenses/>.
 */

// Code generated by e2e/runner/cmd/gen-fixtures; DO NOT EDIT.

`)
	buf.WriteString("import { test as base } from '@playwright/test';\n\n")

	buf.WriteString("type Fixture =\n")
	for i, f := range all {
		fmt.Fprintf(&buf, "  | '%s'", f.Name)
		if i < len(all)-1 {
			buf.WriteString("\n")
		} else {
			buf.WriteString(";\n\n")
		}
	}

	buf.WriteString("export const test = base.extend<{ fixtures: Fixture[] }>({\n")
	buf.WriteString("  fixtures: [[], { option: true }],\n")
	buf.WriteString("});\n")

	outPath := filepath.Join("..", "helpers", "fixtures.ts")
	if err := os.WriteFile(outPath, []byte(buf.String()), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "writing %s: %v\n", outPath, err)
		os.Exit(1)
	}

	cmd := exec.Command("pnpm", "format", filepath.Join("e2e", "helpers", "fixtures.ts"))
	cmd.Dir = filepath.Join("..", "..")
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		fmt.Fprintf(os.Stderr, "formatting %s: %v\n", outPath, err)
		os.Exit(1)
	}

	fmt.Printf("generated %s with %d fixture(s)\n", outPath, len(all))
}
